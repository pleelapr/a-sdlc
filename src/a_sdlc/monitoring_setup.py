def setup_monitoring(force: bool = False) -> tuple[bool, str, dict]:
    """Complete monitoring setup: install files, clone SigNoz, configure settings.

    Args:
        force: If True, force reinstall even if already configured.

    Returns:
        Tuple of (success, message, verification_results)
    """
    messages = []

    # Check prerequisites
    if not check_docker_available():
        return False, (
            "Docker is required for the monitoring stack.\n\n"
            "Please install Docker Desktop: https://www.docker.com/products/docker-desktop/"
        ), {}

    if not check_git_available():
        return False, (
            "Git is required to clone SigNoz.\n\n"
            "Please install git: https://git-scm.com/downloads"
        ), {}

    # Install monitoring files
    success, msg = install_monitoring_files(force=force)
    if not success:
        return False, msg, {}
    messages.append(msg)

    # Clone SigNoz
    success, msg = clone_signoz(force=force)
    if not success:
        return False, msg, {}
    messages.append(msg)

    # Register Stop hook
    success, msg = update_settings_hooks(force=force)
    if not success:
        return False, msg, {}
    messages.append(msg)

    # Set OTEL environment
    success, msg = update_settings_environment(force=force)
    if not success:
        return False, msg, {}
    messages.append(msg)

    # Verify
    verification = verify_monitoring_setup()

    return True, "\n".join(messages), verification
