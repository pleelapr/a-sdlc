def configure_langfuse_keys(
    secret_key: str,
    public_key: str,
    host: str = "http://localhost:13000",
) -> tuple[bool, str]:
    """Add Langfuse API keys to settings.json environment.

    Args:
        secret_key: Langfuse secret key (sk-lf-...)
        public_key: Langfuse public key (pk-lf-...)
        host: Langfuse host URL

    Returns:
        Tuple of (success, message)
    """
    settings = load_claude_settings()
    env = settings.setdefault("environment", {})

    env["LANGFUSE_SECRET_KEY"] = secret_key
    env["LANGFUSE_PUBLIC_KEY"] = public_key
    env["LANGFUSE_HOST"] = host

    try:
        save_claude_settings(settings)
        return True, "Langfuse API keys configured in settings.json"
    except OSError as e:
        return False, f"Failed to save settings: {e}"


