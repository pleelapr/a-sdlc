def clone_signoz(
    target_dir: Path | None = None,
    tag: str = SIGNOZ_TAG,
    force: bool = False,
) -> tuple[bool, str]:
    """Clone SigNoz repository for Docker deployment.

    Args:
        target_dir: Directory to clone into (default: ~/.a-sdlc/monitoring/signoz/)
        tag: Git tag to clone (default: v0.110.1)
        force: If True, remove existing clone and re-clone.

    Returns:
        Tuple of (success, message)
    """
    if target_dir is None:
        target_dir = SIGNOZ_DIR

    if target_dir.exists() and not force:
        return True, f"SigNoz already cloned at {target_dir}"

    if target_dir.exists() and force:
        shutil.rmtree(target_dir)

    try:
        result = subprocess.run(
            [
                "git", "clone",
                "--depth", "1",
                "--branch", tag,
                SIGNOZ_REPO,
                str(target_dir),
            ],
            capture_output=True,
            text=True,
            timeout=300,
        )
        if result.returncode != 0:
            return False, f"git clone failed: {result.stderr.strip()}"

        return True, f"SigNoz {tag} cloned to {target_dir}"

    except subprocess.TimeoutExpired:
        return False, "git clone timed out (>5 minutes)"
    except FileNotFoundError:
        return False, "git not found. Please install git first."


