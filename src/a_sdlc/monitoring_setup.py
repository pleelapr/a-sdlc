def verify_monitoring_setup() -> dict[str, bool | str]:
    """Verify monitoring files and configuration are in place.

    Returns:
        Dict with verification results for each component.
    """
    results: dict[str, bool | str] = {}

    # File checks
    results["monitoring_dir"] = MONITORING_DIR.exists()
    results["langfuse_compose"] = LANGFUSE_COMPOSE_PATH.exists()
    results["langfuse_hook"] = LANGFUSE_HOOK_PATH.exists()
    results["langfuse_env"] = LANGFUSE_ENV_PATH.exists()
    results["docker_compose"] = DOCKER_COMPOSE_PATH.exists()
    results["signoz_cloned"] = SIGNOZ_DIR.exists()

    # Settings checks
    settings = load_claude_settings()
    results["hook_registered"] = _hook_already_registered(settings)

    env = settings.get("environment", {})
    results["otel_configured"] = all(
        key in env for key in OTEL_ENV_VARS
    )
    results["langfuse_keys_configured"] = bool(
        env.get("LANGFUSE_SECRET_KEY") and env.get("LANGFUSE_PUBLIC_KEY")
    )

    # Overall readiness
    results["files_ready"] = all([
        results["langfuse_compose"],
        results["langfuse_hook"],
        results["langfuse_env"],
        results["docker_compose"],
        results["signoz_cloned"],
    ])
    results["settings_ready"] = all([
        results["hook_registered"],
        results["otel_configured"],
    ])
    results["ready"] = bool(results["files_ready"] and results["settings_ready"])

    return results


def check_services_health() -> dict[str, bool | str]:
    """Check if monitoring services are running and reachable.

    Returns:
        Dict with health status for Langfuse and SigNoz.
    """
    import urllib.error
    import urllib.request

    results: dict[str, bool | str] = {}

    # Check Langfuse (port 13000)
    try:
        req = urllib.request.Request("http://localhost:13000", method="HEAD")
        urllib.request.urlopen(req, timeout=5)
        results["langfuse_reachable"] = True
        results["langfuse_url"] = "http://localhost:13000"
    except (urllib.error.URLError, OSError):
        results["langfuse_reachable"] = False
        results["langfuse_url"] = "http://localhost:13000 (not reachable)"

    # Check SigNoz (port 8080)
    try:
        req = urllib.request.Request("http://localhost:8080", method="HEAD")
        urllib.request.urlopen(req, timeout=5)
        results["signoz_reachable"] = True
        results["signoz_url"] = "http://localhost:8080"
    except (urllib.error.URLError, OSError):
        results["signoz_reachable"] = False
        results["signoz_url"] = "http://localhost:8080 (not reachable)"

    results["services_running"] = bool(
        results["langfuse_reachable"] and results["signoz_reachable"]
    )

    return results


