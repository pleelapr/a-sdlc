def _hook_already_registered(settings: dict) -> bool:
    """Check if the langfuse hook is already registered in settings."""
    hooks_config = settings.get("hooks", {})
    stop_hooks = hooks_config.get("Stop", [])

    for matcher_entry in stop_hooks:
        for hook in matcher_entry.get("hooks", []):
            command = hook.get("command", "")
            if "langfuse-hook.py" in command:
                return True

    return False


def update_settings_hooks(force: bool = False) -> tuple[bool, str]:
    """Append Langfuse Stop hook to settings.json.

    Appends to the hooks.Stop[] array without overwriting existing hooks.
    Detects duplicates by checking if any hook command contains
    'langfuse-hook.py'.

    Args:
        force: If True, re-add even if already present.

    Returns:
        Tuple of (success, message)
    """
    settings = load_claude_settings()

    if not force and _hook_already_registered(settings):
        return True, "Langfuse hook already registered"

    # Remove existing langfuse hook entry if force
    if force:
        stop_hooks = settings.get("hooks", {}).get("Stop", [])
        settings.setdefault("hooks", {})["Stop"] = [
            entry for entry in stop_hooks
            if not any(
                "langfuse-hook.py" in h.get("command", "")
                for h in entry.get("hooks", [])
            )
        ]

    # Build the new hook entry
    hook_entry = {
        "matcher": "",
        "hooks": [
            {
                "type": "command",
                "command": HOOK_COMMAND,
            }
        ],
    }

    settings.setdefault("hooks", {}).setdefault("Stop", []).append(hook_entry)

    try:
        save_claude_settings(settings)
        return True, "Langfuse Stop hook registered in settings.json"
    except OSError as e:
        return False, f"Failed to save settings: {e}"


def update_settings_environment(force: bool = False) -> tuple[bool, str]:
    """Merge OTEL environment variables into settings.json.

    Sets CLAUDE_CODE_ENABLE_TELEMETRY, OTEL_METRICS_EXPORTER,
    OTEL_LOGS_EXPORTER, OTEL_EXPORTER_OTLP_PROTOCOL,
    OTEL_EXPORTER_OTLP_ENDPOINT, and OTEL_LOG_TOOL_DETAILS.

    Without force, skips keys that already exist.

    Args:
        force: If True, overwrite existing environment keys.

    Returns:
        Tuple of (success, message)
    """
    settings = load_claude_settings()
    env = settings.setdefault("environment", {})

    added = []
    skipped = []
    for key, value in OTEL_ENV_VARS.items():
        if key in env and not force:
            skipped.append(key)
        else:
            env[key] = value
            added.append(key)

    if not added:
        return True, "OTEL environment already configured"

    try:
        save_claude_settings(settings)
        msg = f"Set {len(added)} OTEL environment variable(s)"
        if skipped:
            msg += f" (skipped {len(skipped)} existing)"
        return True, msg
    except OSError as e:
        return False, f"Failed to save settings: {e}"


